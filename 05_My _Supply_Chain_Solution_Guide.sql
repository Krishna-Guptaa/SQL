-- Q1.  **Savings Analysis:** The company offers discounts. Calculate the total amount saved in each order (Actual Price - Selling Price) and rank orders by total savings.
select oi.orderid, sum((p.unitprice - oi.unitprice)*oi.quantity) as total_amt_saved from product p 
join orderitem oi
on p.id = oi.productid
group by oi.orderid
order by total_amt_saved desc;

