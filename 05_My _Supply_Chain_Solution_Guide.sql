 -- Q1. Arrange the Product ID and Name based on the high demand by the customer
select p.id, p.productname, count(quantity) as high_demand from product p
join orderitem oi
on p.id = oi.productid
group by p.id, p.productname
order by high_demand desc;

-- Q2. Display the total number of orders delivered every year
select count(id) as total_order, year(orderdate) as year from orders
group by year;

-- Q3.  **Revenue Analysis:** Calculate the total revenue generated each year.
select year(orderdate) as year, sum(totalamount) as total_revenue from orders
group by year(orderdate);

-- Q4.  **Top Spender:** Find the details of the customer who has spent the most money across all their orders.
select c.*, sum(o.totalamount) as most_money from customer c
join orders o
on c.id = o.customerid
group by c.id
order by most_money desc
limit 1 ;

-- Q5.  **Customer Ranking:** List all customers and their total order amounts, ordered from highest to lowest.
select c.*, sum(totalamount) as total_order_amounts from customer c
join orders o
on c.id = o.customerid
group by c.id
order by total_order_amounts desc;

-- Q6.  **Order History:** For each customer, list their current and previous order dates to see how frequently they do business.
select c.id as customerid, c.firstname, o.orderdate from customer c
join orders o 
on c.id = o.customerid
group by customerid, orderdate;

-- Q7.  **Supplier Revenue:** Identify the top 3 suppliers based on the total revenue generated by their products.
select s.id, s.contactname , sum(quantity * oi.unitprice) as total_revenue from supplier s
join product p
on s.id = p.supplierid
join orderitem oi
on p.id = oi.productid
join orders o
on o.id = oi.orderid
group by s.id, s.contactname
order by total_revenue desc
limit 3;

-- Q8.  **Customer Loyalty:** Display the latest order date for each customer (excluding their very first order).
select c.*, max(o.orderdate) as latest_order_date from customer c
join orders o 
on c.id = o.customerid
group by c.id;

-- Q9.  **Order Details:** For every order, display the Order ID, Product Name, and Supplier Name.
select o.id as order_id, p.productname as product_name, s.contactname as supplier_name from orders o
join orderitem oi
on o.id = oi.orderid
join product p 
on p.id = oi.productid
join supplier s 
on s.id = p.supplierid;





