-- Q3.  **Premium Suppliers:** Identify companies that supply products with a unit price above 100.
select distinct s.* from supplier s
join product p
on s.id = p.supplierid
join orderitem oi
on p.id = oi.productid
where oi.unitprice > 100;

-- Q4.  **City-Based Networking:** List customers who live in the same city and country, arranged by country.
select distinct c1.id, c1.firstname, c2.firstname, c1.city, c1.country from customer c1
join customer c2
on c1.city = c2.city   and    c1.country = c2.country
where c1.id <> c2.id
order by c1.country;





