use supply_chain;

-- Q1.  **Data Exploration:** Read all the data from the `Supplier`, `Product`, `OrderItem`, `Orders`, and `Customer` tables.
select * from orders;
select * from orderitem;
select * from supplier;
select * from product;
select * from customer;

-- Q2.  **Customer Demographics:** Find the count of customers in each country, ordered from highest to lowest.
select country, count(id) from customer
group by country
order by count(id) desc;

-- Q3.  **Active Inventory:** Display all products that are currently active (not discontinued).
select * from product
where isdiscontinued = "1";

-- Q4.  **Supplier Catalog:** List all company names along with the names of the products they supply.
select s.companyname, p.productname from supplier s
join product p
on s.id = p.supplierid;

-- Q5.  **Regional Search:** Find all customer information for those residing in 'Mexico'.
select * from customer
where country = "Mexico";

-- Q6.  **High-Value Items:** Identify the most expensive item ordered by each customer.
select c.id as customerid, p.productname, p.unitprice from customer c
join orders o
on c.id = o.customerid
join orderitem oi
on oi.orderid = o.id
join product p
on p.id = oi.productid
where (c.id, p.unitprice) in (
select c.id, max(p.unitprice) as most_expensive from product p
join orderitem oi
on oi.productid = p.id
join orders o
on o.id = oi.orderid
join customer c
on c.id = o.customerid
group by c.id
);

-- Q7.  **Top Suppliers:** Find the Supplier ID that provides the highest number of products.     { CTE }
with highest_product as (
select s.id, count(p.id) as count_product from supplier s
join product p
on s.id = p.supplierid
group by s.id
)
select * from highest_product
where count_product in ( select max(count_product) from highest_product);

-- Q8.  **Order Trends:** Display the count of orders placed each month and year.
select year(orderdate) as year, month(orderdate) as month, count(id) as order_count from orders
group by year(orderdate), month(orderdate)
order by year asc, month asc;

-- Q9.  **Supplier Concentration:** Which country has the maximum number of suppliers?
with max_number_supplier as ( 
select country, count(id) as supplier_count from supplier
group by country)
select distinct * from max_number_supplier
where supplier_count in (select max(supplier_count) from max_number_supplier);

