-- Q1.  **Bulk Buyers:** Find customers who have ordered more than 10 products in a single order.
with single_order as (select distinct c.*, count(o.id) as order_id from customer c
join orders o 
on c.id = o.customerid
group by c.id)
select * from single_order
having order_id > 10
order by order_id desc;

